package minimini;

import java.util.Arrays;

public class Arry_2 {
    public static void main(String[] args) {
        int[] arr = {9, 11, 1, 3, 7, 6, 2};
        int sum = 0;
        for (int i = 0; i < arr.length; i++) {
            sum += arr[i];
        }
        System.out.println(sum);



    }
}
